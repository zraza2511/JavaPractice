public class IntExample {
    public static void main(String[] args) {
        int a = 10; // Declaring an integer variable 'a' and assigning the value 10 to it
        int b = 20; // Declaring another integer variable 'b' and assigning the value 20 to it
        
        int sum = a + b; // Adding 'a' and 'b' and storing the result in 'sum'
        
        System.out.println("The sum of a and b is: " + sum); // Printing the sum
        
        // Incrementing a by 1
        a++; // Equivalent to a = a + 1; or a += 1;
        System.out.println("The value of a after increment: " + a); // Printing the updated value of 'a'
    }
}
